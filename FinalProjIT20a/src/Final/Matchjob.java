    private void loadMatchResults() {
    javax.swing.table.DefaultTableModel model =
            (javax.swing.table.DefaultTableModel) jobTable.getModel();

    model.setRowCount(0);

    for (MatchResult m : DataStore.matchResults) {
        model.addRow(new Object[]{
            m.getName(),
            m.getJob()
        });
    }
}

